#include "libft.h"

int		ft_atoi(const char *str)
{
	int		res;
	int		neg;
	int		i;

	res = 0;
	i = 0;
	neg = 1;
	while (str[i] && (str[i] == '\f' || str[i] == '\t' || str[i] == ' '
				|| str[i] == '\n' || str[i] == '\r' || str[i] == '\v'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * neg);
}
